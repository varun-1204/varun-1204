//program for arithmetic operation
#include"stdio.h"
int main()
{
    int num1=100;
    int num2=10;
    printf("num1 +num2=%d\n",num1 + num2);
    printf("num1-num2%d\n",num1-num2);
    printf("num1*num2%d\n",num1*num2);
    printf("num1%%num2%d\n",num1%num2);
    printf("num1/num2%d\n",num1/num2);
    return 0;
} 